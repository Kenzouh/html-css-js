// ternary operator = shortcut to if and else statements.
//                    helps to assign a variable based on a condition.

//                    condition ? outputIfTrue : outputIfFalse;

// Pro: More condensed. Easier to read than if-else statements.


let online = true;

              // Since Boolean, just put the Boolean variable itself in the condition.
onlineStatus = online ? "You're online!" : "You're offline!";

